# This limits range upto which must run
numberTo = 100
# start of the first evenNumber
evenNum = 2
# ensure loops run as long as evenNum<= 100
while evenNum <= numberTo:
# print the current even number    
    print(evenNum)
# Increse by 2 to get the next even number
    evenNum += 2
